#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	d;
	char	*s2;

	s2 = NULL;
	s2 = (char*)s;
	d = 0;
	while (d < n)
	{
		s2[d] = '\0';
		d++;
	}
}
